import os

# функция перехода в папку по указанному пути
def cd(path):
    try:
        #path = input('укажите путь папки, в которую надо перейти: ')
        os.chdir(path)
        print(f'Успешно перешел в {os.path.abspath(path)}')
    except:
        print('Невозможно перейти')

# функция просмотра содержимого текущей папки
def listdr():
    print(os.listdir('.'))

# функция создания указанной папки в текущей директории
def crt_fld(fld_name):
    if not os.path.exists(fld_name):
        try:
            os.mkdir(fld_name)
            print(f'Успешно создана папка {fld_name}')
        except:
            print('Невозможно создать')

# функция удаления указанной папки в текущей директории
def dlt_fld(fld_del):
    try:
        os.rmdir(fld_del)
    except:
        print('Невозможно удалить')

# функция совершения действия по выбору
def algo():
    req = int(input('Что вы хотите сделать:\n '
                    '1 - перейти в папку\n '
                    '2 - посмотреть содержимое\n '
                    '3 - создать папку\n '
                    '4 - удалить папку\n'
                    '0 - закончить\n'))
    if req == 1:
        path = input('укажите путь папки, в которую надо перейти: ')
        cd(path)
        algo()
    elif req == 2:
        print(listdr())
        algo()
    elif req == 3:
        fld_name = str(input('Введите название папки, которую надо создать:'))
        crt_fld(fld_name)
        algo()
    elif req == 4:
        fld_del = str(input('Введите название папки, которую надо удалить:'))
        dlt_fld(fld_del)
        algo()
    elif req == 0:
        print('Завершение')
    else:
        print('Введено некорректное значение')

# инициирую скрипт
algo()