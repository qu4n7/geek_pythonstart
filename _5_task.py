import os
import sys
import time

# создает
for _ in range(1, 10):
    path = 'folder_{}'.format(_)
    if not os.path.exists(path):
        os.mkdir(path)
print('папки созданы, проверьте, пжл, через 10 сек удалятся')
time.sleep(10)

# удаляет
for _ in range(1, 10):
    path = 'folder_{}'.format(_)
    os.rmdir(path)
print('папки удалены')

